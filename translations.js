// Language switcher functionality
let currentLanguage = localStorage.getItem('preferredLanguage') || 'en';

function setLanguage(lang) {
    if (!translations[lang]) return;
    
    currentLanguage = lang;
    localStorage.setItem('preferredLanguage', lang);
    
    const t = translations[lang];
    
    // Update navigation
    document.querySelectorAll('[data-translate="nav.home"]').forEach(el => el.textContent = t.nav.home);
    document.querySelectorAll('[data-translate="nav.about"]').forEach(el => el.textContent = t.nav.about);
    document.querySelectorAll('[data-translate="nav.gallery"]').forEach(el => el.textContent = t.nav.gallery);
    document.querySelectorAll('[data-translate="nav.features"]').forEach(el => el.textContent = t.nav.features);
    document.querySelectorAll('[data-translate="nav.useCases"]').forEach(el => el.textContent = t.nav.useCases);
    document.querySelectorAll('[data-translate="nav.testimonials"]').forEach(el => el.textContent = t.nav.testimonials);
    document.querySelectorAll('[data-translate="nav.bookNow"]').forEach(el => el.textContent = t.nav.bookNow);
    document.querySelectorAll('[data-translate="nav.contact"]').forEach(el => el.textContent = t.nav.contact);
    
    // Update hero section
    const welcomeEl = document.querySelector('[data-translate="hero.welcome"]');
    if (welcomeEl) welcomeEl.textContent = t.hero.welcome;
    
    const subtitleEl = document.querySelector('[data-translate="hero.subtitle"]');
    if (subtitleEl) subtitleEl.textContent = t.hero.subtitle;
    
    const descriptionEl = document.querySelector('[data-translate="hero.description"]');
    if (descriptionEl) descriptionEl.textContent = t.hero.description;
    
    const ctaEl = document.querySelector('[data-translate="hero.cta"]');
    if (ctaEl) ctaEl.textContent = t.hero.cta;
    
    // Update about section
    const aboutTitleEl = document.querySelector('[data-translate="about.title"]');
    if (aboutTitleEl) aboutTitleEl.textContent = t.about.title;
    
    const aboutDesc1 = document.querySelector('[data-translate="about.description1"]');
    if (aboutDesc1) aboutDesc1.textContent = t.about.description1;
    
    const aboutDesc2 = document.querySelector('[data-translate="about.description2"]');
    if (aboutDesc2) aboutDesc2.textContent = t.about.description2;
    
    const aboutDesc3 = document.querySelector('[data-translate="about.description3"]');
    if (aboutDesc3) aboutDesc3.textContent = t.about.description3;
    
    // Update stats labels
    document.querySelectorAll('[data-translate="about.rooms"]').forEach(el => el.textContent = t.about.rooms);
    document.querySelectorAll('[data-translate="about.guests"]').forEach(el => el.textContent = t.about.guests);
    document.querySelectorAll('[data-translate="about.hoursFromAirports"]').forEach(el => el.textContent = t.about.hoursFromAirports);
    document.querySelectorAll('[data-translate="about.minWalkToTrain"]').forEach(el => el.textContent = t.about.minWalkToTrain);
    document.querySelectorAll('[data-translate="about.kmToLuxembourgAirport"]').forEach(el => el.textContent = t.about.kmToLuxembourgAirport);
    document.querySelectorAll('[data-translate="about.kmToBrussels"]').forEach(el => el.textContent = t.about.kmToBrussels);
    document.querySelectorAll('[data-translate="about.bookings"]').forEach(el => el.textContent = t.about.bookings);
    document.querySelectorAll('[data-translate="about.bookedMoreThan"]').forEach(el => el.textContent = t.about.bookedMoreThan);
    document.querySelectorAll('[data-translate="about.movieLocation"]').forEach(el => el.textContent = t.about.movieLocation);
    document.querySelectorAll('[data-translate="about.locationUsedForMovie"]').forEach(el => el.textContent = t.about.locationUsedForMovie);
    document.querySelectorAll('[data-translate="about.allAges"]').forEach(el => el.textContent = t.about.allAges);
    document.querySelectorAll('[data-translate="about.funForYoungAndOld"]').forEach(el => el.textContent = t.about.funForYoungAndOld);
    document.querySelectorAll('[data-translate="about.familyGatherings"]').forEach(el => el.textContent = t.about.familyGatherings);
    document.querySelectorAll('[data-translate="about.idealForLargeFamily"]').forEach(el => el.textContent = t.about.idealForLargeFamily);
    document.querySelectorAll('[data-translate="about.authentic"]').forEach(el => el.textContent = t.about.authentic);
    document.querySelectorAll('[data-translate="about.authenticExperience"]').forEach(el => el.textContent = t.about.authenticExperience);
    
    // Update features section
    const featuresTitleEl = document.querySelector('[data-translate="features.title"]');
    if (featuresTitleEl) featuresTitleEl.textContent = t.features.title;
    
    const featuresSubtitleEl = document.querySelector('[data-translate="features.subtitle"]');
    if (featuresSubtitleEl) featuresSubtitleEl.textContent = t.features.subtitle;
    
    // Update booking section
    const bookingTitleEl = document.querySelector('[data-translate="booking.title"]');
    if (bookingTitleEl) bookingTitleEl.textContent = t.booking.title;
    
    const bookingSubtitleEl = document.querySelector('[data-translate="booking.subtitle"]');
    if (bookingSubtitleEl) bookingSubtitleEl.textContent = t.booking.subtitle;
    
    const bookingDetailsEl = document.querySelector('[data-translate="booking.reservationDetails"]');
    if (bookingDetailsEl) bookingDetailsEl.textContent = t.booking.reservationDetails;
    
    const bookingDescEl = document.querySelector('[data-translate="booking.description"]');
    if (bookingDescEl) bookingDescEl.textContent = t.booking.description;
    
    const benefitsTitleEl = document.querySelector('[data-translate="booking.benefitsTitle"]');
    if (benefitsTitleEl) benefitsTitleEl.textContent = t.booking.benefitsTitle;
    
    document.querySelectorAll('[data-translate="booking.instantConfirmation"]').forEach(el => {
        if (el.classList.contains('feature-title')) el.textContent = t.booking.instantConfirmation;
    });
    document.querySelectorAll('[data-translate="booking.instantConfirmationDesc"]').forEach(el => el.textContent = t.booking.instantConfirmationDesc);
    
    document.querySelectorAll('[data-translate="booking.securePayment"]').forEach(el => {
        if (el.classList.contains('feature-title')) el.textContent = t.booking.securePayment;
    });
    document.querySelectorAll('[data-translate="booking.securePaymentDesc"]').forEach(el => el.textContent = t.booking.securePaymentDesc);
    
    document.querySelectorAll('[data-translate="booking.flexibleCancellation"]').forEach(el => {
        if (el.classList.contains('feature-title')) el.textContent = t.booking.flexibleCancellation;
    });
    document.querySelectorAll('[data-translate="booking.flexibleCancellationDesc"]').forEach(el => el.textContent = t.booking.flexibleCancellationDesc);
    
    document.querySelectorAll('[data-translate="booking.upToGuests"]').forEach(el => {
        if (el.classList.contains('feature-title')) el.textContent = t.booking.upToGuests;
    });
    document.querySelectorAll('[data-translate="booking.upToGuestsDesc"]').forEach(el => el.textContent = t.booking.upToGuestsDesc);
    
    document.querySelectorAll('[data-translate="booking.trustBadge"]').forEach(el => el.textContent = t.booking.trustBadge);
    document.querySelectorAll('[data-translate="booking.secureBadge"]').forEach(el => el.textContent = t.booking.secureBadge);
    
    // Update contact section
    const contactTitleEl = document.querySelector('[data-translate="contact.title"]');
    if (contactTitleEl) contactTitleEl.textContent = t.contact.title;
    
    const contactSubtitleEl = document.querySelector('[data-translate="contact.subtitle"]');
    if (contactSubtitleEl) contactSubtitleEl.textContent = t.contact.subtitle;
    
    // Update form labels
    document.querySelectorAll('[data-translate="contact.name"]').forEach(el => {
        if (el.tagName === 'LABEL') el.textContent = t.contact.name;
    });
    document.querySelectorAll('[data-translate="contact.email"]').forEach(el => {
        if (el.tagName === 'LABEL') el.textContent = t.contact.email;
    });
    document.querySelectorAll('[data-translate="contact.message"]').forEach(el => {
        if (el.tagName === 'LABEL') el.textContent = t.contact.message;
    });
    
    // Update form placeholders
    document.querySelectorAll('[data-translate-placeholder="contact.name"]').forEach(el => el.placeholder = t.contact.name);
    document.querySelectorAll('[data-translate-placeholder="contact.email"]').forEach(el => el.placeholder = t.contact.email);
    document.querySelectorAll('[data-translate-placeholder="contact.phone"]').forEach(el => el.placeholder = t.contact.phone);
    document.querySelectorAll('[data-translate-placeholder="contact.message"]').forEach(el => el.placeholder = t.contact.message);
    
    // Update active language button
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.dataset.lang === lang) {
            btn.classList.add('active');
        }
    });
    
    // Update HTML lang attribute
    document.documentElement.lang = lang;
    
    // Update Lodgify widget language if it exists
    const lodgifyWidget = document.getElementById('lodgify-book-now-box');
    if (lodgifyWidget) {
        const langMap = { en: 'en', nl: 'nl', fr: 'fr' };
        lodgifyWidget.setAttribute('data-language-code', langMap[lang] || 'en');
    }
}

// Add event listeners for language buttons
document.addEventListener('DOMContentLoaded', () => {
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', (e) => {
            e.preventDefault();
            const lang = btn.dataset.lang;
            if (lang) {
                setLanguage(lang);
            }
        });
    });
});

// Initialize language on page load
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', () => setLanguage(currentLanguage));
} else {
    setLanguage(currentLanguage);
}

